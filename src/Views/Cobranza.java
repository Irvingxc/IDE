    private void tblPagosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPagosMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = tblPagos.getSelectedRow(); 
            String Alumno = tblPagos.getValueAt(fila, 0).toString();
            String Cliente = tblPagos.getValueAt(fila, 3).toString();
            String identidad = tblPagos.getValueAt(fila, 1).toString();
            String anio = String.valueOf(java.time.LocalDate.now().getYear());
            
             try {
            Reportes reporte = new Reportes();
             HashMap datos = new HashMap();
             datos.put("pa_alumno", identidad);
             datos.put("pa_anio", anio);
             datos.put("pa_cliente", Cliente);
             datos.put("pa_name_alumno", Alumno);
             
             reporte.ReporteCobranza(datos);
        } catch (JRException ex) {
                 JOptionPane.showMessageDialog(null, "Error en la conexi√≥n a la base de datos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                   ex.printStackTrace();
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        }
             catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error al generar el reporte Jasper: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_tblPagosMouseClicked


